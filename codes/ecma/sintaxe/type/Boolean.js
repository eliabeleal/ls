// literal (true|false)
let variable = false
console.log(variable) //=> false
console.log(typeof variable) //=> boolean

let variable = true
console.log(variable) //=> true
console.log(typeof variable) //=> boolean

// case sensitive
let variable = True // ReferenceError: True is not defined
